    private void btnExcluiServidorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluiServidorActionPerformed
// TODO add your handling code here:
          //Váriaveis para pegar o valor da linha selecionada
          int linha = tabela.getSelectedRow();
          int coluna = tabela.getSelectedColumn(); 
          if(linha == -1){
              JOptionPane.showMessageDialog(null,"Selecione uma Linha","ATENÇÃO",JOptionPane.WARNING_MESSAGE);
          }else{
          //Variáveis para pegar os valores das colunas da jTable
          String codigo = ((DefaultTableModel)tabela.getModel()).getValueAt(linha,0).toString();
          String nome = ((DefaultTableModel)tabela.getModel()).getValueAt(linha,1).toString();
          String setor = ((DefaultTableModel)tabela.getModel()).getValueAt(linha,2).toString();
          
          ClassServidor servidor = new ClassServidor();
          int ConverteCod = Integer.parseInt(codigo);
          servidor.setCodigo(ConverteCod);
          servidor.setDeletaServidor(nome, setor);
          servidor.deletaServidor();
          ((DefaultTableModel)tabela.getModel()).removeRow(linha);//para remover a linha excluída
          }
    }//GEN-LAST:event_btnExcluiServidorActionPerformed

    private void btnListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnListarActionPerformed
// TODO add your handling code here:
      int  linha = tabela.getRowCount();
      int i =0;  
      if(linha > 0){
      while(i < linha){
         ((DefaultTableModel)tabela.getModel()).removeRow(0);
         i++;
       }
      }else{
   try{
         String setor = CBSetor.getSelectedItem().toString();
          //ClassDiaria diaria = new ClassDiaria();
         ClassConecta conexao = new ClassConecta();
         Connection con = conexao.conecta();
         Statement stmt = conexao.con.createStatement();        
         ResultSet rs = stmt.executeQuery("Select * from TabServidor where setor='" + 
                                          setor + "'");
         while(rs.next()){     
              //Adicionando os Dados da tabela na jTable              
             ((DefaultTableModel)tabela.getModel()).addRow(new Object[] {rs.getObject(1),
              rs.getObject(2),rs.getObject(4)});                         
            }
          lblSetor.setText(setor);          
          lblSetor.setVisible(true);
          if(tabela.getRowCount() == 0 ){
        JOptionPane.showMessageDialog(null,"Nenhum Servidor Encontrado","ATENÇÃO",JOptionPane.WARNING_MESSAGE);
              }
       }catch(SQLException e){
           System.out.println(e);          
       }        
      }
    }//GEN-LAST:event_btnListarActionPerformed

