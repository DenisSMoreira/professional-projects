    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

            try{
                String sql ="select * FROM categorias WHERE `codigo` = "+codigo.getText();
                con.executeSQL(sql);
                con.resultset.first();
                String nome = "Tem certeza deseja excluir a categoria:  "+con.resultset.getString("nome")+" ?";
                int opcao_escolhida = JOptionPane.showConfirmDialog(null,nome," Exclusão",JOptionPane.YES_NO_OPTION);

                if ( opcao_escolhida == JOptionPane.YES_OPTION){

                    sql = "DELETE FROM categorias WHERE `codigo` = "+codigo.getText();
                    int conseguiu_excluir = con.statement.executeUpdate(sql);
                    if (conseguiu_excluir == 1){

                        JOptionPane.showMessageDialog(null,"Exclusão da categoria realizada com sucesso! ");
                       con.executeSQL("select * from categorias");
                       PreencherTable();
                       con.resultset.first();
                       mostrar_dados();
                       navega = 1;

                    }
                }

                else{
                    return;
                }
            } catch (SQLException erro) {
                JOptionPane.showMessageDialog(null,"Erro ao excluir o registro");
            }
    }//GEN-LAST:event_jButton3ActionPerformed

