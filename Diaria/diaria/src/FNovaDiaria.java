    private void btnSalvarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSalvarMouseClicked
       if(txtPeriodoSaida.getText().trim().length() == 0 ||
          txtPeriodoChegada.getText().trim().length() ==0 ||
          txtQtdDiaria.getText().trim().length() == 0 ||
          MemoDestino.getText().trim().length() == 0 ||
          MemoObjetivo.getText().trim().length() == 0 ||
          MemoObs.getText().trim().length() == 0){
         //MEnsagem de Alerta  
         JOptionPane.showMessageDialog(null,"Todos os Campos devem ser preenchidos","ATENÇÃO",JOptionPane.WARNING_MESSAGE);
      }else{
        //Variaveis para receber os valores do form
        String nome = CBNome.getSelectedItem().toString();
        String setor = ComboSetor.getSelectedItem().toString();
        String periodoSaida = txtPeriodoSaida.getText();
        String periodoChegada = txtPeriodoChegada.getText();
        String destino = MemoDestino.getText();
        String objetivo = MemoObjetivo.getText();
        String Obs = MemoObs.getText();        
        String relatorio = ComboRelatorio.getSelectedItem().toString(); // iniciei vazia pois vou tratá-la logo abaixo
       int QtdDiaria = Integer.parseInt(txtQtdDiaria.getText());
        // Fim das Variaveis
       ClassDiaria diaria = new ClassDiaria();      
       diaria.setPegaValDiaria(nome, periodoSaida, periodoChegada, destino, relatorio, setor, objetivo, Obs, QtdDiaria);
       diaria.incluiDiaria();
           }
     
    }//GEN-LAST:event_btnSalvarMouseClicked

