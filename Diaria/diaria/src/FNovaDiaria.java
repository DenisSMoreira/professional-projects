    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
// TODO add your handling code here:
        ClassConecta conexao = new ClassConecta();
        try{
          Connection  con = conexao.conecta();
          Statement stmt = con.createStatement();          
          ResultSet rsSetor = stmt.executeQuery("select * from TabCadSetor");          
          while(rsSetor.next()){           
            String setor = rsSetor.getString(2);
            ComboSetor.addItem(setor);
          }          
          rsSetor.close();
          ResultSet rsServidor = stmt.executeQuery("select * from TabServidor");
          while(rsServidor.next()){
              String nome = rsServidor.getString(2);
              CBNome.addItem(nome);
          }
          rsServidor.close();
          stmt.close();
        }catch(SQLException e){
            System.out.println(e);
        }
    }//GEN-LAST:event_formWindowOpened

